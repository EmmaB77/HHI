package Beans;

public class HerramientaBean {
    
    private int idHerramienta;
    private String descHerramienta;
    private int cantidadHerramienta;
    private String tipoUniHerramienta;
    private String observacionesHerramienta;
    private String estatusHerramienta;

    public int getIdHerramienta() {
        return idHerramienta;
    }

    public void setIdHerramienta(int idHerramienta) {
        this.idHerramienta = idHerramienta;
    }

    public String getDescHerramienta() {
        return descHerramienta;
    }

    public void setDescHerramienta(String descHerramienta) {
        this.descHerramienta = descHerramienta;
    }

    public int getCantidadHerramienta() {
        return cantidadHerramienta;
    }

    public void setCantidadHerramienta(int cantidadHerramienta) {
        this.cantidadHerramienta = cantidadHerramienta;
    }

    public String getTipoUniHerramienta() {
        return tipoUniHerramienta;
    }

    public void setTipoUniHerramienta(String tipoUniHerramienta) {
        this.tipoUniHerramienta = tipoUniHerramienta;
    }

    public String getObservacionesHerramienta() {
        return observacionesHerramienta;
    }

    public void setObservacionesHerramienta(String observacionesHerramienta) {
        this.observacionesHerramienta = observacionesHerramienta;
    }

    public String getEstatusHerramienta() {
        return estatusHerramienta;
    }

    public void setEstatusHerramienta(String estatusHerramienta) {
        this.estatusHerramienta = estatusHerramienta;
    }
}
