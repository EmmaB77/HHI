package Beans;

public class EmpresaBean {
    
    private int idEmpresa;
    private String nombreEmpresa;
    private String calleVarchar;
    private String numeroEmpresa;
    private String coloniaEmpresa;
    private String ciudadEmpresa;
    private String estadoEmpresa;
    private String telefonoEmpresa;
    private String mailEmpresa;

    public int getIdEmpresa() {
        return idEmpresa;
    }

    public void setIdEmpresa(int idEmpresa) {
        this.idEmpresa = idEmpresa;
    }

    public String getNombreEmpresa() {
        return nombreEmpresa;
    }

    public void setNombreEmpresa(String nombreEmpresa) {
        this.nombreEmpresa = nombreEmpresa;
    }

    public String getCalleVarchar() {
        return calleVarchar;
    }

    public void setCalleVarchar(String calleVarchar) {
        this.calleVarchar = calleVarchar;
    }

    public String getNumeroEmpresa() {
        return numeroEmpresa;
    }

    public void setNumeroEmpresa(String numeroEmpresa) {
        this.numeroEmpresa = numeroEmpresa;
    }

    public String getColoniaEmpresa() {
        return coloniaEmpresa;
    }

    public void setColoniaEmpresa(String coloniaEmpresa) {
        this.coloniaEmpresa = coloniaEmpresa;
    }

    public String getCiudadEmpresa() {
        return ciudadEmpresa;
    }

    public void setCiudadEmpresa(String ciudadEmpresa) {
        this.ciudadEmpresa = ciudadEmpresa;
    }

    public String getEstadoEmpresa() {
        return estadoEmpresa;
    }

    public void setEstadoEmpresa(String estadoEmpresa) {
        this.estadoEmpresa = estadoEmpresa;
    }

    public String getTelefonoEmpresa() {
        return telefonoEmpresa;
    }

    public void setTelefonoEmpresa(String telefonoEmpresa) {
        this.telefonoEmpresa = telefonoEmpresa;
    }

    public String getMailEmpresa() {
        return mailEmpresa;
    }

    public void setMailEmpresa(String mailEmpresa) {
        this.mailEmpresa = mailEmpresa;
    }
    
}
